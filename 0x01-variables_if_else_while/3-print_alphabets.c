#include <stdio.h>

/**
 * main - prints the lowercase followed by uppercase of all alpabetical \
 * characters
 * Return: return zero
 */
int main(void)
{
	char c;

	c = 'a';
	while (c <= 'z')
	{
		putchar(c);
		c++;
	}
	c = 'A';
	while (c <= 'Z')
	{
		putchar(c);
		c++;
	}
	putchar('\n');
	return (0);
}
